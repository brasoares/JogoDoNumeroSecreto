/* Crie um contador que começa em 10 e vá até 0 usando um loop while. Mostre cada número. */
let num = 10;

while (num > 0) {
    num--;
    alert(num);
    console.log(num); // This is also an important line of code so that a coder is able to validate via browser each interacion
    // such validation (abovementioned) might be done without the "alert" option, in general, or depending on the situation, of course!
}